# Dobro

def dobro(nu):
    r = nu * 2
    return r 

    


#Quadrado
    
def quad(qu):
    d = qu ** 4 
    return d


# inicio
    
nu = int(input("Me Mostre número: "))

respostadobro = dobro(nu)
print(f"O dobro do seu número foi {respostadobro}")

respostaquadrada = quad(nu)
print(f"O dobro do seu número foi {respostaquadrada}")